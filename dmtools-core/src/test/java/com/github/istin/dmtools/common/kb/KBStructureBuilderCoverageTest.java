    @Test
    void testBuildTopicFiles_CollectsFromExistingFilesAndAnalysis() throws IOException {
        // Existing question file: answered, with a non-system tag
        Path questionsDir = tempDir.resolve("questions");
        Files.createDirectories(questionsDir);
        Files.writeString(questionsDir.resolve("q_0001.md"),
                "---\n"
                + "id: \"q_0001\"\n"
                + "author: \"alice\"\n"
                + "topics: [\"Docker\"]\n"
                + "answeredBy: \"a_0001\"\n"
                + "tags: [\"#question\", \"#source_x\", \"buildkit\"]\n"
                + "---\n\nbody\n");
        // Unreadable entry (directory with .md name) -> IOException is swallowed
        Files.createDirectories(questionsDir.resolve("unreadable.md"));

        // Existing answer linked to a question + standalone answer + answer only in files
        Path answersDir = tempDir.resolve("answers");
        Files.createDirectories(answersDir);
        Files.writeString(answersDir.resolve("a_0001.md"),
                "---\n"
                + "id: \"a_0001\"\n"
                + "author: \"bob\"\n"
                + "topics: [\"Docker\"]\n"
                + "answersQuestion: \"q_0001\"\n"
                + "tags: [\"#answer\", \"caching\"]\n"
                + "---\n\nbody\n");
        Files.writeString(answersDir.resolve("a_0003.md"),
                "---\n"
                + "id: \"a_0003\"\n"
                + "author: \"dave\"\n"
                + "topics: [\"Docker\"]\n"
                + "answersQuestion: \"q_0001\"\n"
                + "---\n\nbody\n");
        Files.createDirectories(answersDir.resolve("broken.md"));

        // Existing note with single-quoted id (quote-stripping branch)
        Path notesDir = tempDir.resolve("notes");
        Files.createDirectories(notesDir);
        Files.writeString(notesDir.resolve("n_0002.md"),
                "---\n"
                + "id: 'n_0002'\n"
                + "author: \"erin\"\n"
                + "topics: [\"Docker\"]\n"
                + "tags: [\"#note\"]\n"
                + "---\n\nbody\n");
        Files.createDirectories(notesDir.resolve("corrupt.md"));

        // Current analysis: q answered by a_0001, a_0002 standalone, n_0001 answers q_0001
        Question q = new Question();
        q.setId("q_0001");
        q.setAuthor("alice");
        q.setArea("Dev");
        q.setTopics(Arrays.asList("Docker"));
        q.setAnsweredBy("a_0001");
        q.setTags(Arrays.asList("buildkit"));

        Answer a1 = new Answer();
        a1.setId("a_0001");
        a1.setAuthor("bob");
        a1.setArea("Dev");
        a1.setTopics(Arrays.asList("Docker"));
        a1.setAnswersQuestion("q_0001");

        Answer a2 = new Answer();
        a2.setId("a_0002");
        a2.setAuthor("bob");
        a2.setArea("Dev");
        a2.setTopics(Arrays.asList("Docker"));

        Note n1 = new Note();
        n1.setId("n_0001");
        n1.setAuthor("carol");
        n1.setArea("Dev");
        n1.setTopics(Arrays.asList("Docker"));
        n1.setAnswersQuestions(Arrays.asList("q_0001"));

        // Note answering a question that is NOT in this topic -> stays standalone
        Note n3 = new Note();
        n3.setId("n_0003");
        n3.setAuthor("frank");
        n3.setArea("Dev");
        n3.setTopics(Arrays.asList("Docker"));
        n3.setAnswersQuestions(Arrays.asList("q_9999"));

        AnalysisResult analysis = new AnalysisResult();
        analysis.setQuestions(Arrays.asList(q));
        analysis.setAnswers(Arrays.asList(a1, a2));
        analysis.setNotes(Arrays.asList(n1, n3));

        builder.buildTopicFiles(analysis, tempDir, "src");

        Path topicFile = tempDir.resolve("topics").resolve("docker.md");
        assertTrue(Files.exists(topicFile), "Topic file should be created");
        String content = Files.readString(topicFile);

        // Aggregated non-system tags end up in frontmatter (sorted)
        assertTrue(content.contains("tags: [\"buildkit\", \"caching\"]"), "Should aggregate sorted tags");
        // q_0001 has both answer and note -> listed under Questions with Answers
        assertTrue(content.contains("## Questions with Answers"), "Should have Q&A section");
        assertTrue(content.contains("![[q_0001]]"), "Should embed answered question");
        // a_0001 (from analysis) and a_0003 (from existing files) both reference q_0001 -> excluded
        assertFalse(content.contains("![[a_0001]]"), "Linked answer should be excluded from standalone");
        assertFalse(content.contains("![[a_0003]]"), "File-only linked answer should be excluded from standalone");
        // a_0002 has no question reference -> standalone answer
        assertTrue(content.contains("## Additional Answers"), "Should have standalone answers section");
        assertTrue(content.contains("![[a_0002]]"), "Standalone answer should be embedded");
        // n_0001 answers q_0001 -> excluded from standalone notes;
        // n_0002 (from existing files) has no answersQuestions -> standalone note
        assertTrue(content.contains("## Notes"), "Should have standalone notes section");
        assertTrue(content.contains("![[n_0002]]"), "Standalone note from files should be embedded");
        assertFalse(content.contains("![[n_0001]]"), "Note answering a question should be excluded");
        // Description placeholder created
        assertTrue(Files.exists(tempDir.resolve("topics").resolve("docker-desc.md")),
                "Desc placeholder should be created");
    }

