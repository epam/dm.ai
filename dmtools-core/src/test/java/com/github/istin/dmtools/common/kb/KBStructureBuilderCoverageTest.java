    @Test
    void testGeneratePeopleIndex_WithPeople() throws IOException {
        Files.createDirectories(tempDir.resolve("people").resolve("Bob_Smith"));
        Files.createDirectories(tempDir.resolve("people").resolve("Alice_Brown"));
        // Non-directory entry should be ignored
        Files.writeString(tempDir.resolve("people").resolve("stray.md"), "x");

        builder.generatePeopleIndex(tempDir);

        Path peopleFile = tempDir.resolve("people").resolve("people.md");
        assertTrue(Files.exists(peopleFile), "people.md should be created");
        String content = Files.readString(peopleFile);
        assertTrue(content.contains("**Total contributors:** 2"), "Should count only directories");
        assertTrue(content.contains("![[Alice_Brown/Alice_Brown]]"), "Should embed Alice profile");
        assertTrue(content.contains("![[Bob_Smith/Bob_Smith]]"), "Should embed Bob profile");
        assertTrue(content.indexOf("Alice_Brown") < content.indexOf("Bob_Smith"),
                "People should be sorted alphabetically");
        assertFalse(content.contains("stray"), "Files should be ignored");
        assertFalse(content.trim().endsWith("---"), "Trailing separator should be removed");
    }
