    @Test
    void testBuildAreaStructure_ReadsExistingAreaFileWithSingleContributor() throws IOException {
        // Existing area file with single-value contributors frontmatter and single-quoted title
        Path areaDir = tempDir.resolve("areas").resolve("dev");
        Files.createDirectories(areaDir);
        Files.writeString(areaDir.resolve("dev.md"),
                "---\n"
                + "type: area\n"
                + "title: 'Dev'\n"
                + "id: dev\n"
                + "sources: [\"old_source\"]\n"
                + "contributors: alice\n"
                + "created: \"2024-01-01T00:00:00Z\"\n"
                + "---\n\n"
                + "# Dev\n\n"
                + "## Topics\n\n"
                + "- [[docker|Docker]]\n\n"
                + "## Key Contributors\n\n");

        // A "broken" area whose .md is a directory -> read fails and is logged/skipped
        Files.createDirectories(tempDir.resolve("areas").resolve("broken").resolve("broken.md"));

        Question q = new Question();
        q.setId("q_0001");
        q.setAuthor("bob");
        q.setArea("Dev");
        q.setTopics(Arrays.asList("Kubernetes"));

        AnalysisResult analysis = new AnalysisResult();
        analysis.setQuestions(Arrays.asList(q));

        builder.buildAreaStructure(analysis, tempDir, "new_source");

        Path areaFile = areaDir.resolve("dev.md");
        String content = Files.readString(areaFile);
        assertTrue(content.contains("\"old_source\""), "Should keep existing source");
        assertTrue(content.contains("\"new_source\""), "Should add current source");
        assertTrue(content.contains("alice"), "Should keep existing contributor from frontmatter");
        assertTrue(content.contains("bob"), "Should add current contributor");
        assertTrue(content.contains("[[docker|Docker]]"), "Should keep topic from existing file");
        assertTrue(content.contains("[[kubernetes|Kubernetes]]"), "Should add new topic");
        assertTrue(content.contains("created: \"2024-01-01T00:00:00Z\""), "Should preserve created timestamp");
        // Broken area should not crash the build
        assertTrue(Files.isDirectory(tempDir.resolve("areas").resolve("broken")));
    }

