    @Test
    void testUpdateTopicWithStats_ReplacesAutoGeneratedSection() throws IOException {
        Path topicDir = tempDir.resolve("topics").resolve("docker");
        Files.createDirectories(topicDir);
        Path topicFile = topicDir.resolve("docker.md");
        Files.writeString(topicFile, "# Docker\n\n"
                + "<!-- AUTO_GENERATED_START -->\n"
                + "old stats\n"
                + "<!-- AUTO_GENERATED_END -->\n");

        builder.updateTopicWithStats(tempDir, "docker", 3, 2, 1, Arrays.asList("bob", "alice"));

        String content = Files.readString(topicFile);
        assertTrue(content.contains("- Questions: 3"), "Should contain questions count");
        assertTrue(content.contains("- Answers: 2"), "Should contain answers count");
        assertTrue(content.contains("- Notes: 1"), "Should contain notes count");
        assertTrue(content.contains("## Key Contributors"), "Should contain contributors section");
        // Contributors must be sorted alphabetically
        assertTrue(content.indexOf("[[alice|alice]]") < content.indexOf("[[bob|bob]]"),
                "Contributors should be sorted");
        assertFalse(content.contains("old stats"), "Old section should be replaced");
    }

